"""
Nombre del ejercicio: Contador

Instrucciones: 
1.- Usa el ciclo while para iterar números hasta que el iterador sea menor que una operación dada
2.- Imprime el conteo 

"""

a = 20
b = 2
operacion = a*b

contador = 0

while operacion > contador:
    print(F"contador es: {contador}")
    contador += 1